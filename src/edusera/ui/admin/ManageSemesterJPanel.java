    private void populateTable() {
        currentSemester();
    
        DefaultTableModel model = (DefaultTableModel) tblCourses.getModel();
        model.setRowCount(0);
        for ( Semester sem: business.getSemesterList()){
            Object[] row = new Object[5];
            row[0] = sem;
            row[1] = sem.getStartsAt();
            row[2] = sem.getEndsAt();
            model.addRow(row);
        }
  }

    private void currentSemester() {
        lblCurrentSem.setText("Current Semester: " + business.getCurrent());
    }
