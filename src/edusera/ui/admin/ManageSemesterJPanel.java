    private void btnCurrentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCurrentActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblCourses.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Select a semester first");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblCourses.getModel();
        Semester sem = (Semester) model.getValueAt(selectedRowIndex, 0);
        business.setCurrent(sem);
        JOptionPane.showMessageDialog(this, "The current semester is now "+ sem);
        populateTable();
    }//GEN-LAST:event_btnCurrentActionPerformed


