    private void btnViewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnViewMouseClicked
        // TODO add your handling code here:
   
         btnUpdate.setEnabled(true);
         btnDelete.setEnabled(true);
         
        int selectedRowIndex = tblStudents.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Select a course to register");
        }
        DefaultTableModel model = (DefaultTableModel) tblStudents.getModel();
        selectedStudent = (Student)model.getValueAt(selectedRowIndex, 0);
        txtName.setText(selectedStudent.getName());
        txtEmail.setText(selectedStudent.getEmail());
        txtnuId.setText(selectedStudent.getNuId());
        txtnuId.setEnabled(false);
      
    }//GEN-LAST:event_btnViewMouseClicked

    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        // TODO add your handling code here:
         int selectedRowIndex = tblStudents.getSelectedRow();
        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete!");
         }
        DefaultTableModel model = (DefaultTableModel) tblStudents.getModel();
        selectedStudent = (Student)model.getValueAt(selectedRowIndex, 0);
        
        business.getStudentDir().deletePerson(selectedStudent);
        JOptionPane.showMessageDialog(this, "Student Deleted!");
        
        resetPage();
    }//GEN-LAST:event_btnDeleteMouseClicked

