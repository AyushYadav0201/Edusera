    private void populateTable() {
    
       DefaultTableModel model = (DefaultTableModel) tblStudents.getModel();
       model.setRowCount(0);
    
    for (Student student : business.getStudentDir().getUsers()) {
         Object[]row = new Object[6];
            row[0]= student;
            row[1]=student.getNuId();
            row[2]=student.getEmail();
           
            //model will add row
            model.addRow(row);
          }
    }
  

  private void populateTableSearch(String nuId) {
        DefaultTableModel model = (DefaultTableModel) tblStudents.getModel(); //Convert to TableModel to defaultTableModel
        model.setRowCount(0);
        
         for (Student student : business.getStudentDir().getUsers()){
            
            if(!student.getNuId().equals(nuId))
                continue;
            
            //to create lines or row (row is small array of object with 3 members)
            Object[]row = new Object[6];
            row[0]= student;
            row[1]=student.getNuId();
            row[2]=student.getEmail();       
            //model will add row
            model.addRow(row);
          }
   
}

