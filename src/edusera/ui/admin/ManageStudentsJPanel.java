    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        int selectedRowIndex = tblStudents.getSelectedRow();
        
        if(selectedRowIndex < 0){
            
        JOptionPane.showMessageDialog(this, "Please select a row to update");
            
        } else{
            
        DefaultTableModel model = (DefaultTableModel) tblStudents.getModel();
        selectedStudent = (Student)model.getValueAt(selectedRowIndex, 0);

       
        String name = txtName.getText();
        String email = txtEmail.getText();
        String nuId = txtnuId.getText();
        
        if(!checkValidate()){
         JOptionPane.showMessageDialog(this, "Please enter valid details");
    }
        else{
        selectedStudent.setName(name);
        selectedStudent.setEmail(email);
        selectedStudent.setNuId(nuId);
 
        JOptionPane.showMessageDialog(this, "Row updated successfully");
         resetPage();
            
        }
       
        }  
    }//GEN-LAST:event_btnUpdateMouseClicked

