    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        try {
            // TODO add your handling code here:
            String name = txtName.getText();
            String email = txtEmail.getText();
            String password = txtPassword.getText();
            String nuId = txtNUID.getText();
            Object role  =  cmbRole.getSelectedItem();
            
            if(!checkValidate()){
                JOptionPane.showMessageDialog(this, "Please enter valid details !!");
                return;
            } else {
                
            
            switch(role.toString()){
                case "Student":
                Student student = business.getStudentDir().addStudent(nuId, name, email);
                business.getUserDir().addUser(nuId, email, password, student, Role.STUDENT, true);
                break;
                
                case "Professor":
                Professor professor = business.getProfDir().addProfessor(nuId, name, email);
                User profAccount = new User(nuId, email, password, professor, Role.PROFESSOR, true);
                business.getUserDir().addUser(nuId, name, password, professor, Role.PROFESSOR, true);
                break;
            }
            
            JOptionPane.showMessageDialog(this, "Sign up completed!");
            clearFields();
            }
        } catch (Exception ex) {
            Logger.getLogger(SignUpJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
      
    }//GEN-LAST:event_btnSubmitActionPerformed
    
